export interface Glow {
  /**
   * The color of the glow.
   */
  color?: string;

  /**
   * The x offset of the glow.
   */
  x?: number;

  /**
   * The y offset of the glow.
   */
  y?: number;

  /**
   * The blur radius of the glow.
   */
  blur?: number;

  /**
   * The opacity of the glow.
   */
  opacity?: number;
}
